import java.util.Scanner;

class primtall{


    public static void main(String args[]){ 

        boolean loop = true;
        while (loop) {
            Scanner input_1 = new Scanner(System.in);
            System.out.println("Skriv inn et tall: ");
            int num = input_1.nextInt();
            
            boolean primtall_check = true;
            for (int i = 2; i < num; i++) {
                if (num%i==0){
                    primtall_check = false;
                    break;
                }
            } if (primtall_check) {
                System.out.println(num+" er et primtall!!");
            } else {
                System.out.println(num+" er IKKE et primtall!!");
            }

            boolean loop2 = true;
            while (loop2){
                Scanner input_2 = new Scanner(System.in);
                System.out.println("Skriv '1' hvis du vil fortsette, og '2' hvis du er ferdig: ");
                int option = input_2.nextInt();
                if (option == 1){
                    loop = true;
                    loop2 = false;
                }
                if (option == 2){
                    loop = false;
                    loop2 = false;
                }
                System.out.println("\n\n\n\n\n\n\n\n\n");
                }
                }
    }
}


